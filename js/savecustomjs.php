<?php
require_once(__DIR__ . '/../vendor/dashticz/security.php');

dashticz_require_same_origin();
dashticz_require_csrf();

if (!isset($_SERVER['REQUEST_METHOD']) || $_SERVER['REQUEST_METHOD'] !== 'POST') {
    dashticz_json_error(405, 'Only POST requests are allowed.');
}

$customDir = __DIR__ . '/../custom';
$configPath = $customDir . '/CONFIG.js';
$defaultConfigPath = $customDir . '/CONFIG_DEFAULT.js';

if (file_exists($configPath)) {
    dashticz_json_error(409, 'CONFIG.js already exists.');
}

if (!is_dir($customDir)) {
    if (!mkdir($customDir, 0775, true)) {
        dashticz_json_error(500, 'The directory "custom/" does not exist and could not be created. ' .
            'Create it manually: sudo mkdir -p custom/ && sudo chown root:www-data custom/ && sudo chmod 2775 custom/');
    }
    // Try to apply the correct group and permissions when PHP is the owner.
    @chmod($customDir, 2775);
}

if (!is_writable($customDir)) {
    // Try via the optional privilege-escalation helper (requires a sudoers rule; see
    // tools/dashticz-fix-custom-permissions for installation instructions).
    $helperPath = '/usr/local/sbin/dashticz-fix-custom-permissions';
    if (is_executable($helperPath)) {
        $helperOutput = [];
        $helperExit   = 0;
        exec('sudo ' . escapeshellarg($helperPath) . ' 2>&1', $helperOutput, $helperExit);
        clearstatcache(true, $customDir);
    }

    if (!is_writable($customDir)) {
        dashticz_json_error(500, 'The directory "custom/" is not writable by the web server' .
            dashticz_owner_info($customDir) .
            '. Run: sudo chown root:www-data custom/ && sudo chmod 2775 custom/');
    }
}

$allowedKeys = [
    'domoticz_ip', 'app_title', 'theme', 'domoticz_refresh', 'dashticz_refresh',
    'login_timeout', 'editmode', 'loginEnabled', 'client_id', 'client_secret',
    'enable_websocket', 'room_plan', 'use_cors', 'default_cors_url', 'dashticz_php_path',
    'standby_after', 'background_image', 'start_page', 'enable_swiper',
    'vertical_scroll', 'auto_swipe_back_to', 'auto_swipe_back_after', 'auto_slide_pages',
    'slide_effect', 'standard_graph', 'blink_color', 'language',
    'timeformat', 'calendarformat', 'calendarlanguage',
    'auto_positioning', 'use_favorites', 'disable_googleanalytics',
    'last_update', 'hide_topbar', 'edit_mode',
];
$lines = [];

foreach ($allowedKeys as $key) {
    if (!array_key_exists($key, $_POST)) {
        continue;
    }
    $value = json_decode($_POST[$key], true);
    if (json_last_error() !== JSON_ERROR_NONE || is_array($value) || is_object($value)) {
        dashticz_json_error(400, 'Invalid value for setting ' . $key . '.');
    }
    $lines[] = 'config[' . json_encode($key) . '] = ' .
        json_encode($value, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE) . ';';
}

if (empty($lines)) {
    dashticz_json_error(400, 'No valid settings provided.');
}

$generatedContent = "/* Dashticz setup - generated by setup wizard */\n" .
    implode("\n", $lines) . "\n";

$content = $generatedContent;
if (file_exists($defaultConfigPath)) {
    $defaultContent = file_get_contents($defaultConfigPath);
    if ($defaultContent === false) {
        dashticz_json_error(500, 'Could not read CONFIG_DEFAULT.js.');
    }
    $content = rtrim($defaultContent) . "\n\n" . $generatedContent;
}

if (file_put_contents($configPath, $content, LOCK_EX) === false) {
    dashticz_json_error(500, 'Could not write CONFIG.js. Check that the web server has write permissions on the "custom/" directory.');
}
// Apply group-writable permissions so www-data can overwrite the file later
// (relevant when PHP ran as root or a different user during first setup).
chmod($configPath, 0664);

header('Content-Type: application/json');
echo json_encode(array('success' => true));
